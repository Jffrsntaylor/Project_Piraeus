class YardConfig:
    def __init__(self, length, width, height, energy_consumption_rate, carbon_emission_factor, 
                 max_weight_per_stack, crane_speed, crane_energy_consumption):
        self.length = length
        self.width = width
        self.height = height
        self.energy_consumption_rate = energy_consumption_rate
        self.carbon_emission_factor = carbon_emission_factor
        self.max_weight_per_stack = max_weight_per_stack
        self.crane_speed = crane_speed
        self.crane_energy_consumption = crane_energy_consumption
